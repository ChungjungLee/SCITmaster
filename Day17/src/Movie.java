public class Movie {
	private String title;
	private String director;
	private String company;
	
	public Movie() {
		title = null;
		director = null;
		company = null;		
	}
	
	public Movie(String title, String director, String company) {
		this.title = title;
		this.director = director;
		this.company = company;
	}
}
